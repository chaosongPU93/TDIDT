function y = linefcn(x,a,b)
% y = linefcn(x,a,b)
%
% This function outputs y of a straight line based on the 
% input x, slope a and intercept b
%
% A line can be expressed many ways, but always can be
% generalized to y = a.*x+b
%
% By Chao Song, chaosong@princeton.edu
% First created date:   2020/11/18
% Last modified date:   2020/11/18

y = a .*x + b;